package com.hc.logic.config;

public class LevelConfig {

	private int id;  //也表示等级
	private int exp; //最大经验
	private int hp;  //最大血量
	private int mp;  //最大法力
	private int uHp; //每秒恢复血量
	private int uMp; //每秒恢复法力
	private int lAttack; //等级对应的攻击力
	
	
	
	
	
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getMp() {
		return mp;
	}
	public void setMp(int mp) {
		this.mp = mp;
	}
	public int getuHp() {
		return uHp;
	}
	public void setuHp(int uHp) {
		this.uHp = uHp;
	}
	public int getuMp() {
		return uMp;
	}
	public void setuMp(int uMp) {
		this.uMp = uMp;
	}
	public int getlAttack() {
		return lAttack;
	}
	public void setlAttack(int lAttack) {
		this.lAttack = lAttack;
	}
	
	
	
	
	
}
